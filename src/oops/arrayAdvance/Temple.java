package oops.arrayAdvance;

public class Temple{


    // Instance variables
    private String name;
    private String address;
    private int steps;

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for address (fixed return type)
    public void setAddress(String address) {
        this.address = address;
    }

    // Getter for address
    public String getAddress() {
        return address;
    }

    // Setter for steps
    public void setSteps(int steps) {
        this.steps = steps;
    }

    // Getter for steps (corrected method name)
    public int getSteps() {
        return steps;
    }
}
