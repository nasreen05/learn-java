package basic.asciiConcept;

public class CharAcsValue {
    public static void main(String[] args){
        for(int i='a';i<='z';i++){
      System.out.println(i+ " ascii value: "+(int)i);

        }
        for(int i='A';i<='Z';i++){
      System.out.println((char)i+" ascii Value: "+(int)i);
        }
    }
}
